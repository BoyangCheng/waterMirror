export const getResumeScreeningPrompt = (
  jobDescription: string,
  resumeText: string,
) => `请根据以下职位描述，分析这份简历，提取关键信息并评估匹配度。

### 职位描述:
${jobDescription}

### 简历内容:
${resumeText}

请严格按以下JSON格式返回（不要返回任何其他内容，不要使用markdown代码块）:
{
  "name": "申请人姓名",
  "company": "最近/当前公司名称",
  "position": "最近/当前职位",
  "phone": "申请人手机/电话号码，如简历中未提供则返回null",
  "summary": "一句话亮点总结，必须完整覆盖 7 个关键词并凝练成一句通顺自然的中文句子（不是关键词罗列）",
  "score": 85
}

### "summary" 字段生成规则（重要，必须严格执行）
1. 先从简历中挑出 7 个最能体现候选人竞争力的关键词，覆盖但不限于以下维度（同一维度最多 1 个关键词，确保信息多元）：
   - 工作年限 / 资历层级
   - 核心技术栈或专业方向
   - 领域经验（行业 / 业务场景）
   - 最有分量的 1 家公司或项目
   - 可量化的成果（数字 / 规模 / 影响）
   - 管理或协作能力（带人、跨团队、主导项目等）
   - 突出加分项（学历、证书、开源、专利、语言能力等）
2. 如果某个维度简历里完全没有信息，就换一个能真正体现亮点的维度补齐，最终一定要凑满 7 个关键词。
3. 把这 7 个关键词凝练成**一句通顺自然的中文**放进 "summary"，要求：
   - 只能是一个句子，句末一个句号
   - 不要输出"关键词："这种前缀，也不要用顿号硬拼，要像人写简介一样自然衔接
   - 长度控制在 40–70 个汉字之间
   - 示例："拥有 10 年互联网后端经验，精通 Go 与微服务架构，曾在字节跳动主导日均百亿请求的支付系统，带过 8 人团队，并获得阿里云 ACE 认证。"

评分标准 (0-100):
- 90-100: 技能、经验、行业高度匹配，是理想候选人
- 70-89: 大部分技能匹配，经验相关，值得面试
- 50-69: 部分技能匹配，有一定相关性
- 30-49: 匹配度较低，仅少量技能相关
- 0-29: 几乎不匹配`;
